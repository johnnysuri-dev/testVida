        private void JuegoDeLaVida()
        {
            int[,] celulasTemp = new int[longitud, longitud];
            for (int x = 0; x < longitud; x++)
            {
                for (int y = 0; y < longitud; y++)
                {
                    if (celulas[x, y] == 0)
                        celulasTemp[x, y] = ReglaJuegoVida(x, y, false);
                    else
                        celulasTemp[x, y] = ReglaJuegoVida(x, y, true);
                }
            }

            celulas = celulasTemp;

        }

        private int ReglaJuegoVida(int x, int y, bool EsViva)
        {
            int VecinasVivas = 0;

            //vecina 1
            if (x > 0 && y > 0)
                if (celulas[x - 1, y - 1] == 1)
                    VecinasVivas++;

            //vecina 2
            if (y > 0)
                if (celulas[x, y - 1] == 1)
                    VecinasVivas++;

            //vecina 3
            if (x < longitud - 1 && y > 0)
                if (celulas[x + 1, y - 1] == 1)
                    VecinasVivas++;

            //vecina 4
            if (x > 0)
                if (celulas[x - 1, y] == 1)
                    VecinasVivas++;

            //vecina 5
            if (x < longitud - 1)
                if (celulas[x + 1, y] == 1)
                    VecinasVivas++;

            //vecina 6
            if (x > 0 && y < longitud - 1)
                if (celulas[x - 1, y + 1] == 1)
                    VecinasVivas++;

            //vecina 7
            if (y < longitud - 1)
                if (celulas[x, y + 1] == 1)
                    VecinasVivas++;


            //vecina 8
            if (x < longitud - 1 && y < longitud - 1)
                if (celulas[x + 1, y + 1] == 1)
                    VecinasVivas++;

            if (EsViva)
                return (VecinasVivas == 2 || VecinasVivas == 3) ? 1 : 0;
            else
                return VecinasVivas == 3 ? 1 : 0;

        }


